package day25_Loops;

public class ForLoop {
    public static void main(String[] args) {

        for(int i = 0; i<=5; i++){
            System.out.println("hello world");

        }
        for(int i =1;i<=10;i++){    //i++ is called update statement or increment
            System.out.println(i);
        }
    }
}
