package day33_VoidMethods;

public class GetCharcters {
    public static void FromAToZ(){
        for (char i = 'A'; i <='Z'; i++) {
            System.out.print(i+ " ");
        }
        System.out.println();

    }
    public static void FromaToz(){
        for (char i ='z';i>='a';i--){
            System.out.print(i+ " ");
        }
        System.out.println();
    }
    public static void print0To9(){
        for (char i = '1'; i <= '9' ; i++) {
            System.out.print(i + " ");
        }
        System.out.println();

        }

}
